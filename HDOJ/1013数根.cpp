/*
1.一位数模9是它本身(9除外);整十数模9是它十位的数字(90除外);整百数摸9是它百位的数字(900除外)......
2.对于一个一般的数,可以分拆成几个10的幂的和;一般的数模9的结果,实质就是题目所描述的"数字根"(9的倍数除外).
*/

#include "stdio.h"
int main()
{
	char s[100000];
	int i;
    int m;

	while(scanf("%s",s)!=EOF)
	{
		m=0;
	   if(s[0]=='0')break;
	   for(i=0;s[i]!='\0';i++)
	   {
		   m+=s[i]-'0';
	   }
	   m=m%9;
	   if(m==0)m=9;
	   printf("%d\n",m);
	}
	return 0;
}